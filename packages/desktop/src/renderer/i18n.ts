const translations: Record<Lang, Dict> = { en, pt, es, fr, de, it, ru, zh, ja, hi, ar };

function detect(): Lang {
  const stored = localStorage.getItem("tc-lang") as Lang | null;
  if (stored && translations[stored]) return stored;
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase() as Lang;
  return translations[nav] ? nav : "en";
}

let current: Lang = detect();
const subs = new Set<() => void>();

function applyDocument(lang: Lang): void {
  document.documentElement.lang = lang;
  document.documentElement.dir = RTL.has(lang) ? "rtl" : "ltr";
}
applyDocument(current);

export function getLang(): Lang {
  return current;
}

export function setLang(lang: Lang): void {
  if (lang === current) return;
  current = lang;
  localStorage.setItem("tc-lang", lang);
  applyDocument(lang);
  for (const fn of subs) fn();
}

export function t(key: string, vars?: Record<string, string | number>): string {
  let s = translations[current]?.[key] ?? en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}

export function useI18n(): { lang: Lang; setLang: (l: Lang) => void; t: typeof t } {
  const lang = useSyncExternalStore(
    (cb) => {
      subs.add(cb);
      return () => subs.delete(cb);
    },
    () => current,
    () => current,
  );
  return { lang, setLang, t };
}
